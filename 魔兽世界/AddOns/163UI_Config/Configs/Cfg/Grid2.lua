U1RegisterAddon("Grid2", {
    title = "Grid2 团队框架",
    tags = { "TAG_INTERFACE", },
    desc = "Grid2 团队框架",
    load = "NORMAL",
    defaultEnable = 0,
    nopic = 1,
    conflicts = { "VuhDo", },
    minimap = 'LibDBIcon10_Grid2', 
    icon = [[Interface\Addons\Grid2\media\icon]],

    runBeforeLoad = function(info, name)
        CoreMakeAce3DBSingleProfile("Grid2DB", U1GetCfgValue("Grid2", "accoutwide"));
    end,

    {
        text = "帐号统一配置",
        var = "accoutwide",
        tip = "开启时，将使用帐号通用配置`关闭时，将重置当前角色设置为开启前的状态",
        default = false,
        callback = function(cfg, v, loading)
            if not loading and Grid2DB ~= nil then
                local key = UnitName('player') .. " - " .. GetRealmName();
                if v then
                    local pk = Grid2DB.profileKeys[key];
                    Grid2DB.profiles.Default = Grid2DB.profiles.Default or Grid2DB.profiles[pk];
                    Grid2DB.profiles[pk] = nil;
                    Grid2DB.profileKeys[key] = "Default";
                else
                    Grid2DB.profileKeys[key] = nil;
                end
            end
        end,
        reload = 1,
    },

    {
        type = 'button',
        text = '载入预设的通用配置',
        callback = function()
            Grid2:OpenGrid2Options();
            if _163_Grid2_ImportProfile then
                _163_Grid2_ImportProfile(nil, profile, true, nil);
            end
        end,
    },

    {
        text = "配置选项",
        callback = function(cfg, v, loading)
            if Grid2:OpenGrid2Options() then
                Grid2Options:OnChatCommand()
            end
        end
    }
});
